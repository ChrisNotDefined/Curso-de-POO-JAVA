    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        
        clsOperCalendario objOperCalendario =  new clsOperCalendario();
        
        objOperCalendario.DeterminarMes(Integer.parseInt(txtMes.getText())
                                       , Integer.parseInt(txtAnio.getText()));
        
        objOperCalendario.Calcular();
        
        
        txtDia.setText(Integer.toString(objOperCalendario.getN()));// getDia
        
        p = "";
        
        p += "\t\t\t"+objOperCalendario.getMesn()+"\n";//getMes
        
        p += "Domingo\tLunes\tMartes\tMiercoles\tJueves\tViernes\tSabado\n";
        
        //Operacion
        for ( j = 1; j <= objOperCalendario.getN(); j++) {
            p+= "\t";
        }
        
        for ( i = 1; i <= objOperCalendario.getDm(); i++) {
            p += Integer.toString(i)+"\t";
            
            if(i==7-objOperCalendario.getN()
                    ||i==14-objOperCalendario.getN()
                    ||i==21-objOperCalendario.getN()
                    ||i==28-objOperCalendario.getN()
                    ||i==35-objOperCalendario.getN()){
                p+="\n";
            }
        }
        
        
        //Setear el valor de p en el TextArea 
        txtCalendario.setText(p);
        
    }//GEN-LAST:event_btnAceptarActionPerformed

